        i = t * (MG.length - 1),
        l = Math.floor(i),
        h = Math.min(l + 1, MG.length - 1),
        f = i - l;
    return [
        Math.round(MG[l][0] + (MG[h][0] - MG[l][0]) * f),
        Math.round(MG[l][1] + (MG[h][1] - MG[l][1]) * f),
        Math.round(MG[l][2] + (MG[h][2] - MG[l][2]) * f),
    ];
